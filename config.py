def stemtokenize(document):
    """
    Given a document (represented as a string), return a list of all of the
    words in that document, in order.

    Process document by coverting all words to lowercase, and removing any
    punctuation or English stopwords WITH STEMMING.
    """
    tokens=[]
    sentence=word_tokenize(document)

    for word in sentence:
        lowered=word.lower()
        for letter in word:
            if not letter.isalnum():
                lowered=lowered.replace(letter,"")
        if lowered not in stopwords and lowered != '' and not lowered.isnumeric():
            tokens.append(porter.stem(lowered))
    return tokens

